public class DebugDataTypes2 {
    public static void main(String[] args) {
    char letter = 'A';
    short index = 0;
    boolean answer = true;
    String message = "I got this";

    System.out.print("Char data type: ");
    System.out.println(letter);

    System.out.print("Short data type: ");
    System.out.println(index);

    System.out.print("Boolean data type: ");
    System.out.println(answer);

    System.out.print("String data type: ");
    System.out.println(message);

    }
}
